%EXAMPLE_ST_MP_WU
% Script example pipeline ST MP WU

clear variables %#ok<*NASGU>

%% Load BrainAtlas
im_ba = ImporterBrainAtlasXLS( ...
    'FILE', [fileparts(which('example_ST_MP_WU')) filesep 'Example data ST_MP XLS' filesep 'atlas.xlsx'], ...
    'WAITBAR', true ...
    );

ba = im_ba.get('BA');

%% Load Groups of SubjectST_MP
im_gr1 = ImporterGroupSubjectST_MP_XLS( ...
    'DIRECTORY', [fileparts(which('SubjectST_MP')) filesep 'Example data ST_MP XLS' filesep 'ST_MP_Group_1_XLS'], ...
    'BA', ba, ...
    'WAITBAR', true ...
    );

gr1 = im_gr1.get('GR');

im_gr2 = ImporterGroupSubjectST_MP_XLS( ...
    'DIRECTORY', [fileparts(which('SubjectST_MP')) filesep 'Example data ST_MP XLS' filesep 'ST_MP_Group_2_XLS'], ...
    'BA', ba, ...
    'WAITBAR', true ...
    );

gr2 = im_gr2.get('GR');

%% Analysis ST MP WU
a_WU1 = AnalyzeGroup_ST_MP_WU( ...
    'GR', gr1, ...
    'CORRELATION_RULE', Correlation.PEARSON ...
    );

a_WU2 = AnalyzeGroup_ST_MP_WU( ...
    'TEMPLATE', a_WU1, ...
    'GR', gr2 ...
    );

% measure calculation
g_WU1 = a_WU1.get('G');
degree_WU1 = g_WU1.get('MEASURE', 'Degree').get('M');
% % % ovstrength_WU1 = g_WU1.get('MEASURE', 'OverlappingStrength').get('M');
% % % ovstrength_av_WU1 = g_WU1.get('MEASURE', 'OverlappingStrengthAv').get('M');

g_WU2 = a_WU2.get('G');
degree_WU2 = g_WU2.get('MEASURE', 'Degree').get('M');
% % % ovstrength_WU2 = g_WU2.get('MEASURE', 'OverlappingStrength').get('M');
% % % ovstrength_av_WU2 = g_WU2.get('MEASURE', 'OverlappingStrengthAv').get('M');

% comparison
c_WU = CompareGroup( ...
    'P', 10, ...
    'A1', a_WU1, ...
    'A2', a_WU2, ...
    'WAITBAR', true, ...
    'VERBOSE', false, ...
    'MEMORIZE', true ...
    );

degree_WU_diff = c_WU.get('COMPARISON', 'Degree').get('DIFF');
degree_WU_p1 = c_WU.get('COMPARISON', 'Degree').get('P1');
degree_WU_p2 = c_WU.get('COMPARISON', 'Degree').get('P2');
degree_WU_cil = c_WU.get('COMPARISON', 'Degree').get('CIL');
degree_WU_ciu = c_WU.get('COMPARISON', 'Degree').get('CIU');

% % % ovstrength_WU_diff = c_WU.get('COMPARISON', 'OverlappingStrength').get('DIFF');
% % % ovstrength_WU_p1 = c_WU.get('COMPARISON', 'OverlappingStrength').get('P1');
% % % ovstrength_WU_p2 = c_WU.get('COMPARISON', 'OverlappingStrength').get('P2');
% % % ovstrength_WU_cil = c_WU.get('COMPARISON', 'OverlappingStrength').get('CIL');
% % % ovstrength_WU_ciu = c_WU.get('COMPARISON', 'OverlappingStrength').get('CIU');

% % % ovstrength_av_WU_diff = c_WU.get('COMPARISON', 'OverlappingStrengthAv').get('DIFF');
% % % ovstrength_av_WU_p1 = c_WU.get('COMPARISON', 'OverlappingStrengthAv').get('P1');
% % % ovstrength_av_WU_p2 = c_WU.get('COMPARISON', 'OverlappingStrengthAv').get('P2');
% % % ovstrength_av_WU_cil = c_WU.get('COMPARISON', 'OverlappingStrengthAv').get('CIL');
% % % ovstrength_av_WU_ciu = c_WU.get('COMPARISON', 'OverlappingStrengthAv').get('CIU');
